package com.example.th4_14_10;

public class itemSach {
    public itemSach(String name, int img) {
        this.name = name;
        this.img = img;
    }

    String name;
    int img;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
